public class BookInfo
{
    int id;
    String name;
    int price;

    void setId(int id)
    {
       this.id=id;
    }
    int getId()
    {
       return id;
    }

    void setName(String name)
    {
       this.name=name;
    }
    String getName()
    {
       return name;
    }

    void setPrice(int price)
    {
       this.price=price;
    }
    int getPrice()
    {
       return price;
    }

}
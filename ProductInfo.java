public class ProductInfo
{
    int id;
    int price;
    String name;
 
    void setId(int id){
        this.id=id;  
    }
    int getId(){
        return id;
    }

    void setPrice(int price){
        this.price=price;  
    }
    int getPrice(){
        return price;
    }

    void setName(String name){
        this.name=name;  
    }
    String getName(){
        return name;
    }

}